package com.stc.sqm.hajj;

public class UtilReadings {

	private String circuit;
	private String utilDate;
	private String utilIn;
	private String utilOut;
	
	public String getCircuit() {
		return circuit;
	}
	public void setCircuit(String circuit) {
		this.circuit = circuit;
	}
	public String getUtilDate() {
		return utilDate;
	}
	public void setUtilDate(String utilDate) {
		this.utilDate = utilDate;
	}
	public String getUtilIn() {
		return utilIn;
	}
	public void setUtilIn(String utilIn) {
		this.utilIn = utilIn;
	}
	public String getUtilOut() {
		return utilOut;
	}
	public void setUtilOut(String utilOut) {
		this.utilOut = utilOut;
	}
	
}
